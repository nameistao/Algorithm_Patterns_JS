import func from "./index.js";

test("test 1", () => {
  expect(func([1, 3, 5])).toEqual(
    new Set([
      [1, 3, 5],
      [1, 5, 3],
      [3, 1, 5],
      [3, 5, 1],
      [5, 1, 3],
      [5, 3, 1],
    ])
  );
});

